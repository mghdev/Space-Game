class Pair(object):
	def __init__(self,value):
		self._value = list(value[:2])

	@property
	def x(self):
		return self._value[0]

	@x.setter
	def x(self,value):
		self._value[0] = value

	@property
	def y(self):
		return self._value[1]

	@y.setter
	def y(self,value):
		self._value[1] = value



